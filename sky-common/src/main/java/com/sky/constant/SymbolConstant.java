package com.sky.constant;

/**
 * 标准符号工具类
 */
public class SymbolConstant {

    // 常用符号
    public static final String COMMA = ",";              // 逗号
    public static final String DOT = ".";                // 点
    public static final String COLON = ":";              // 冒号
    public static final String SEMICOLON = ";";          // 分号
    public static final String UNDERSCORE = "_";         // 下划线
    public static final String DASH = "-";               // 短横线
    public static final String SLASH = "/";              // 斜杠
    public static final String BACKSLASH = "\\";         // 反斜杠
    public static final String PIPE = "|";               // 竖线
    public static final String AT = "@";                 // @
    public static final String HASH = "#";               // #
    public static final String DOLLAR = "$";             // $
    public static final String PERCENT = "%";            // %
    public static final String AMPERSAND = "&";          // &
    public static final String STAR = "*";               // 星号
    public static final String QUESTION = "?";           // 问号
    public static final String EXCLAMATION = "!";        // 感叹号
    public static final String QUOTE = "\"";             // 双引号
    public static final String APOSTROPHE = "'";         // 单引号
    public static final String LEFT_BRACKET = "(";       // 左圆括号
    public static final String RIGHT_BRACKET = ")";      // 右圆括号
    public static final String LEFT_BRACE = "{";         // 左花括号
    public static final String RIGHT_BRACE = "}";        // 右花括号
    public static final String LEFT_SQUARE = "[";        // 左方括号
    public static final String RIGHT_SQUARE = "]";       // 右方括号
    public static final String LESS_THAN = "<";          // 小于号
    public static final String GREATER_THAN = ">";       // 大于号
    public static final String EQUALS = "=";             // 等号

    // 特殊字符
    public static final String SPACE = " ";              // 空格
    public static final String TAB = "\t";               // 制表符
    public static final String NEWLINE = "\n";           // 换行符（Unix）
    public static final String CARRIAGE_RETURN = "\r";   // 回车符（Windows 常用 \r\n）
}
